# Build features: Education + Experience + Remote
skills_for_clustering = top24

def standard_scale(df_in: pd.DataFrame):
    df = df_in.copy()
    means = df.mean(axis=0)
    stds = df.std(axis=0).replace(0, np.nan)  # avoid divide-by-zero
    return ((df - means) / stds).fillna(0.0)

# Compute per-skill averages for the three features
skill_avg_edu = {
    s: df.loc[df[s] == 1, "education_required_encoded"].mean()
    for s in skills_for_clustering if s in df.columns
}
skill_avg_exp = {
    s: df.loc[df[s] == 1, "experience_level_encoded"].mean()
    for s in skills_for_clustering if s in df.columns
}
skill_avg_remote = {
    s: df.loc[df[s] == 1, "remote_category_encoded"].mean()
    for s in skills_for_clustering if s in df.columns
}

# Create the feature table (three columns)
avg_df = pd.DataFrame({
    "AvgEducation": pd.Series(skill_avg_edu),
    "AvgExperience": pd.Series(skill_avg_exp),
    "AvgRemote": pd.Series(skill_avg_remote),
}).loc[skills_for_clustering]

# Scale to mean=0, std=1
km_scaled_df = standard_scale(avg_df)


# Numpy matrix for KMeans
X = km_scaled_df.values

print("Final features shape (should be N_skills x 3):", km_scaled_df.shape)
km_scaled_df.head()

"""בטבלה המוצגת לעיל מוצגים הפיצ'רים שנבחרו כקלט לאלגוריתם האשכול, ובהם נשתמש לצורך ביצוע הקלאסטרינג.

בחירת ערך של K:
"""

# Elbow method

from sklearn.cluster import KMeans
import matplotlib.pyplot as plt

sse = []
K_range = range(2, 20)  # Try different K values

for k in K_range:
    kmeans = KMeans(n_clusters=k, random_state=42, n_init=10)
    kmeans.fit(km_scaled_df)
    sse.append(kmeans.inertia_)  # Inertia = sum of squared distances to cluster centers

# Plot Elbow curve
plt.figure(figsize=(6, 4))
plt.plot(K_range, sse, marker='o')
plt.title("Elbow Method for Optimal K")
plt.xlabel("Number of Clusters (K)")
plt.ylabel("Sum of squared distances (Inertia)")
plt.grid(True)
plt.show()

# Run KMeans with chosen K
k_optimal = 6  # from elbow or decision

km = KMeans(n_clusters=k_optimal, random_state=42, n_init=10)
km.fit(km_scaled_df)

# Cluster centers (scaled space)
