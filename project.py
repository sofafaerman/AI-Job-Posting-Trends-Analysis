print(f"רווח הסמך ברמת מובהקות של 95%: [{ci_lower:.2f}, {ci_upper:.2f}]")

mean_with    = df.loc[tested_skill_mask,  col_name].dropna().mean()
mean_without = df.loc[~tested_skill_mask, col_name].dropna().mean()
param_mean = mean_with - mean_without

# תצוגה של היסטוגרמת ההתפלגויות של הפרש הממוצעים
