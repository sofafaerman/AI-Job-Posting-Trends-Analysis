"""# חלק 3

בדיקת השערות על השאלה שלנו באמצעות bootstrap.
"""

def bootstrap_mean(sample, mask, col_name, num_replications):
    sample_mask = mask.loc[sample.index]
    with_skill = sample.loc[sample_mask, col_name].dropna().reset_index(drop=True)
    without_skill = sample.loc[~sample_mask, col_name].dropna().reset_index(drop=True)
    sample_size_with = with_skill.shape[0]
    sample_size_without = without_skill.shape[0]
    bootstrap_means = np.empty(num_replications)
    for i in range(num_replications):
        bootstrap_sample_with = with_skill.sample(sample_size_with, replace=True)
        bootstrap_sample_without = without_skill.sample(sample_size_without, replace=True)
        bootstrap_means[i] = bootstrap_sample_with.mean() - bootstrap_sample_without.mean()
    return bootstrap_means

# בחירת מיומנות לבדיקה: הכי נפוצה שיש לה שתי קבוצות מספיק גדולות
min_per_group = 50
top_skill = None
for s in top_skills:
    if s in df.columns:
        m = (df[s] == 1)
        if m.sum() >= min_per_group and (~m).sum() >= min_per_group:
            top_skill = s
            break
if top_skill is None:
    top_skill = top_skills[0]
print(f"מיומנות נבדקת: {top_skill}")

col_name = "salary_usd"
tested_skill_mask = (df[top_skill] == 1)
NUM_REP = 10000
sample = df.sample(n=50000, random_state=42)
means_bootstrapped = bootstrap_mean(sample, tested_skill_mask, col_name, NUM_REP)
# CI אחוזוני 95% להפרש
